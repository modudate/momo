-- 모임 소개 문구 = 예약 상품의 "상세 소개"를 따라가도록
--  · 기존에는 일정 생성 시 상품의 소개가 일정으로 복사돼서,
--    상품의 상세 소개를 나중에 수정해도 이미 만든 일정에는 반영되지 않았음.
--  · 이제 일정 생성 시 복사하지 않는다 → 새 일정은 상품의 상세 소개를 그대로 따라감.
--  · 아래는 "상품과 완전히 똑같은 복사본"만 비운다. (실행 완료)
--    일정별로 다르게 적힌 문구는 관리자가 의도적으로 덮어쓴 것일 수 있어 건드리지 않는다.
--    → 낡은 문구를 지우려면 관리자 > 예약관리 > 일정 수정에서 소개 문구를 비우면 됨.
-- ============================================================
update public.meetings m
   set description = null
  from public.moim_templates t
 where m.template_id = t.id
   and m.description is not null
   and btrim(coalesce(m.description, '')) = btrim(coalesce(t.description, ''));
